#ifndef	__VIDEOTYPE__
#define	__VIDEOTYPE__

//Video Type
enum
{
	VS_VIDEOTYPE			=	0x1000,
	//
	VS_VIDEOTYPE_ENC		=	0x0100,
	VS_VIDEOTYPE_H264		=	VS_VIDEOTYPE | VS_VIDEOTYPE_ENC | 0x00000001,
	VS_VIDEOTYPE_MPEG4		=	VS_VIDEOTYPE | VS_VIDEOTYPE_ENC | 0x00000002,
	VS_VIDEOTYPE_MJPEG		=	VS_VIDEOTYPE | VS_VIDEOTYPE_ENC | 0x00000003,
	//RAW
	VS_VIDEOTYPE_RAW		=	0x0200,
	VS_VIDEOTYPE_RGB32		=	VS_VIDEOTYPE | VS_VIDEOTYPE_RAW | 0x00000004,
	VS_VIDEOTYPE_YUV420		=	VS_VIDEOTYPE | VS_VIDEOTYPE_RAW | 0x00000005,
	VS_VIDEOTYPE_YUV422		=	VS_VIDEOTYPE | VS_VIDEOTYPE_RAW | 0x00000006,

	//201311
	VS_VIDEOTYPE_RGB565		=	VS_VIDEOTYPE | VS_VIDEOTYPE_RAW | 0x00000007,
	VS_VIDEOTYPE_RGB24		=	VS_VIDEOTYPE | VS_VIDEOTYPE_RAW | 0x00000008,

	VS_VIDEOTYPE_NODEFINED	=	VS_VIDEOTYPE | 0x000000FF
};
//Audio Type
enum
{
	VS_AUDIOTYPE			=	0x2000,
	VS_AUDIOTYPE_PCMU		=	VS_AUDIOTYPE | 0x00000001,
	VS_AUDIOTYPE_PCMA		=	VS_AUDIOTYPE | 0x00000002,
	VS_AUDIOTYPE_NODEFINED	=	VS_AUDIOTYPE | 0x000000FF
};

//Event Type
enum
{
	VS_EVENT_DISCONNECT	=	0,
	VS_EVENT_CLOSE			 ,	//RTSP_CLOSE

	VS_EVENT_UNDEF			 ,
	VS_EVENT_MOTION			 ,

	VS_EVENT_VA_PC			 ,
};

#define	ISVIDEORAWTYPE(nFrameType) ( (nFrameType & 0xFF00) == ( VS_VIDEOTYPE | VS_VIDEOTYPE_RAW ) ? 1 : 0)
#define	ISVIDEOTYPE(nFrameType) ( (nFrameType & 0xF000) == VS_VIDEOTYPE ? 1 : 0)
#define	ISAUDIOTYPE(nFrameType) ( (nFrameType & 0xF000) == VS_AUDIOTYPE ? 1 : 0)
#define	ISDEFINEDTYPE(nFrameType) ( ( ISVIDEOTYPE(nFrameType) || ISAUDIOTYPE(nFrameType) ) && (nFrameType & 0xFF) != 0x00 && (nFrameType & 0xFF) != 0xFF )

#endif	//__VIDEOTYPE__
